package security

import (
	"testing"

	"github.com/stretchr/testify/assert"
)

func TestEncryptPassword(t *testing.T) {
	t.Run("ValidPassword", func(t *testing.T) {
		password := "securepassword"
		hashed, err := EncryptPassword(password)
		assert.NoError(t, err)
		assert.NotEmpty(t, hashed)

		err = VerifyPassword(hashed, password)
		assert.NoError(t, err)
	})

	t.Run("InvalidPassword", func(t *testing.T) {
		password := "securepassword"
		hashed, err := EncryptPassword(password)
		assert.NoError(t, err)

		err = VerifyPassword(hashed, "wrongpassword")
		assert.Error(t, err)
	})

	t.Run("EncryptPassword bcrypt: password length exceeds 72 bytes", func(t *testing.T) {
		password := "C897E8FC-47B8-4DA9-A185-E4D780CA6612C897E8FC-47B8-4DA9-A185-E4D780CA6612C897E8FC-47B8-4DA9-A185-E4D780CA6612C897E8FC-47B8-4DA9-A185-E4D780CA6612C897E8FC-47B8-4DA9-A185-E4D780CA6612C897E8FC-47B8-4DA9-A185-E4D780CA6612C897E8FC-47B8-4DA9-A185-E4D780CA6612C897E8FC-47B8-4DA9-A185-E4D780CA6612C897E8FC-47B8-4DA9-A185-E4D780CA6612C897E8FC-47B8-4DA9-A185-E4D780CA6612C897E8FC-47B8-4DA9-A185-E4D780CA6612C897E8FC-47B8-4DA9-A185-E4D780CA6612C897E8FC-47B8-4DA9-A185-E4D780CA6612C897E8FC-47B8-4DA9-A185-E4D780CA6612C897E8FC-47B8-4DA9-A185-E4D780CA6612C897E8FC-47B8-4DA9-A185-E4D780CA6612C897E8FC-47B8-4DA9-A185-E4D780CA6612C897E8FC-47B8-4DA9-A185-E4D780CA6612C897E8FC-47B8-4DA9-A185-E4D780CA6612C897E8FC-47B8-4DA9-A185-E4D780CA6612C897E8FC-47B8-4DA9-A185-E4D780CA6612C897E8FC-47B8-4DA9-A185-E4D780CA6612C897E8FC-47B8-4DA9-A185-E4D780CA6612C897E8FC-47B8-4DA9-A185-E4D780CA6612C897E8FC-47B8-4DA9-A185-E4D780CA6612C897E8FC-47B8-4DA9-A185-E4D780CA6612C897E8FC-47B8-4DA9-A185-E4D780CA6612C897E8FC-47B8-4DA9-A185-E4D780CA6612C897E8FC-47B8-4DA9-A185-E4D780CA6612C897E8FC-47B8-4DA9-A185-E4D780CA6612C897E8FC-47B8-4DA9-A185-E4D780CA6612C897E8FC-47B8-4DA9-A185-E4D780CA6612C897E8FC-47B8-4DA9-A185-E4D780CA6612"
		hashed, err := EncryptPassword(password)
		assert.Error(t, err)
		assert.Empty(t, hashed)
	})
}
